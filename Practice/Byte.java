class Byte {
    public static void main(String[] args) {
        byte ageChild = 5;
        byte numberOfRooms = 2;
        byte examGrade = 3;
        byte floorLevel = 4;
        byte numberOfTickets = 10;

        System.out.println("Initial Byte values: Age = " + ageChild + ", Rooms = " + numberOfRooms + ", Grade = " + examGrade + ", Floor Level = " + floorLevel + ", Tickets = " + numberOfTickets);

        ageChild = 8;
        numberOfRooms = 3;
        examGrade = 4;
        floorLevel = 5;
        numberOfTickets = 12;

        System.out.println("Updated Byte values: Age = " + ageChild + ", Rooms = " + numberOfRooms + ", Grade = " + examGrade + ", Floor Level = " + floorLevel + ", Tickets = " + numberOfTickets);
    }
}
