class Integer {
    public static void main(String[] args) {
        int apples = 5;
        int bananas = 10;
        int oranges = 7;
        int mangoes = 3;
        int grapes = 12;
        
        System.out.println("Initial Fruit Counts: Apples = " + apples + ", Bananas = " + bananas + ", Oranges = " + oranges + ", Mangoes = " + mangoes + ", Grapes = " + grapes);

        apples = 8;
        bananas = 6;
        oranges = 5;
        mangoes = 4;
        grapes = 10;
        
        System.out.println("Updated Fruit Counts: Apples = " + apples + ", Bananas = " + bananas + ", Oranges = " + oranges + ", Mangoes = " + mangoes + ", Grapes = " + grapes);
    }
}
