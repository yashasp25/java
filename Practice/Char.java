class Char {
    public static void main(String[] args) {
        char card = 'K';
        char bloodGroup = 'O';
        char section = 'A';
        char direction = 'N';
        char classRoom = 'L';

        System.out.println("Initial Values: Card = " + card + ", Blood Group = " + bloodGroup + ", Section = " + section + ", Direction = " + direction + ", Class Room = " + classRoom);

        card = 'Q';
        bloodGroup = 'B';
        section = 'C';
        direction = 'S';
        classRoom = 'H';

        System.out.println("Updated Values: Card = " + card + ", Blood Group = " + bloodGroup + ", Section = " + section + ", Direction = " + direction + ", Class Room = " + classRoom);
    }
}
